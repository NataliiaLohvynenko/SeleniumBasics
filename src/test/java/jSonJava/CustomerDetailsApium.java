package jSonJava;

public class CustomerDetailsApium {

    private String courseName;
    private String purchasedDete;
    private int amount;
    private String location;

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getPurchasedDete() {
        return purchasedDete;
    }

    public void setPurchasedDete(String purchasedDete) {
        this.purchasedDete = purchasedDete;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        location = location;
    }

}
